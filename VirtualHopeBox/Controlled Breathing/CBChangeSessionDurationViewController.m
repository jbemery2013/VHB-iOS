#import "CBChangeSessionDurationViewController.h"

@interface CBChangeSessionDurationViewController () {
    NSArray *durations, *durationNames;
}

@end

@implementation CBChangeSessionDurationViewController

@synthesize delegate;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    [self.tableView registerNib:[UINib nibWithNibName:@"CBBasicTableCell" bundle:nil] forCellReuseIdentifier:@"Cell"];
    
    durations = [NSArray arrayWithObjects:[NSNumber numberWithFloat:0], [NSNumber numberWithFloat:120], [NSNumber numberWithFloat:300], [NSNumber numberWithFloat:600], [NSNumber numberWithFloat:900], nil];
    durationNames = [NSArray arrayWithObjects:@"Unlimited", @"2 Minutes", @"5 Minutes", @"10 Minutes", @"15 Minutes", nil];

    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
 
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
}

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [durations count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (!cell) {
        cell = [[[NSBundle mainBundle] loadNibNamed:@"CBBasicTableCell" owner:nil options:nil] objectAtIndex:0];
    }
    cell.textLabel.text = [durationNames objectAtIndex:indexPath.row];
    
    return cell;
}


- (void)viewDidUnload
{
    [super viewDidUnload];
    // Release any retained subviews of the main view.
    // e.g. self.myOutlet = nil;
}

- (BOOL)shouldAutorotateToInterfaceOrientation:(UIInterfaceOrientation)interfaceOrientation
{
    return (interfaceOrientation == UIInterfaceOrientationPortrait);
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [delegate durationChanged:[durations objectAtIndex:indexPath.row]];
    [self dismissViewControllerAnimated:YES completion:nil];
}

@end
